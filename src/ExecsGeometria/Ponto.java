package ExecsGeometria;

class Ponto {

    double xPonto;
    double yPonto;

    Ponto(double x, double y) {
        xPonto = x;
        yPonto = y;
    }

    public double getxPonto() {
        return xPonto;
    }

    public void setxPonto(double xPonto) {
        this.xPonto = xPonto;
    }

    public double getyPonto() {
        return yPonto;
    }

    public void setyPonto(double yPonto) {
        this.yPonto = yPonto;
    }
}
